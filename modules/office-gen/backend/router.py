# Capability: office-gen:replace_existing
# Generate Office file and replace an existing file entry
# =====================================================================
async def _cap_replace_existing(params: dict, caller: str) -> dict:
    """Generate an office file and replace an existing file entry.
    No base64 needed — system handles binary internally.
    Returns updated file info with version tracking.
    """
    owner_id = resolve_caller_user_id(caller)
    format_type = params.get("format", "xlsx")
    filename = params.get("filename", "未命名文档")
    target_file_id = int(params.get("target_file_id", 0))

    if target_file_id <= 0:
        raise ValueError("target_file_id must be a positive integer")

    if format_type == "xlsx":
        sheets = params.get("sheets", []) or params.get("工作表", [])
        data = {"filename": filename, "sheets": sheets}
        bytes_data = generator.generate_xlsx(data)
    elif format_type == "docx":
        content = params.get("content", [])
        data = {"filename": filename, "content": content}
        bytes_data = generator.generate_docx(data)
    elif format_type == "pptx":
        slides = params.get("slides", []) or params.get("幻灯片", [])
        data = {"filename": filename, "slides": slides}
        bytes_data = generator.generate_pptx(data)
    elif format_type == "pdf":
        content = params.get("content", [])
        data = {"filename": filename, "content": content}
        bytes_data = generator.generate_pdf(data)
    else:
        raise ValueError(f"Unsupported format: {format_type}")

    async with AsyncSessionLocal() as db:
        result = await replace_file_content(db, target_file_id, owner_id, bytes_data)

        # Update Content Package after replacing the physical file.
        content_package_id = None
        try:
            from app.services.content.package_service import ContentPackageService
            from app.services.content.pipeline_service import ContentPipelineService

            pkg_svc = ContentPackageService()
            pipeline_svc = ContentPipelineService()
            pkg = await pkg_svc.get_or_create(db, target_file_id, owner_id, caller)
            if pkg:
                content_package_id = pkg["id"]
                await pipeline_svc.run_pipeline_for_package(db, pkg["id"], caller)
        except Exception as e:
            logger.warning("Failed to update parsed content package for replaced file: %s", e)

    return {
        "file_id": target_file_id,
        "content_package_id": content_package_id,
        "name": result["name"],
        "size": result["size"],
        "format": format_type,
        "status": "replaced",
    }


# =====================================================================
# Capability: office-gen:export_to_artifact
# Convert an existing file to an artifact
# =====================================================================
async def _cap_export_to_artifact(params: dict, caller: str) -> dict:
    """Export an existing office file as an artifact record for version management."""
    owner_id = resolve_caller_user_id(caller)
    file_id = int(params.get("file_id", 0))

    if file_id <= 0:
        raise ValueError("file_id must be a positive integer")

    # Check if there's already a Content Package for this file
    content_package_id = None
    async with AsyncSessionLocal() as db:
        try:
            from app.models.content import ContentPackage
            from sqlalchemy import select
            cp_r = await db.execute(
                select(ContentPackage).where(
                    ContentPackage.source_file_id == file_id,
                    ContentPackage.deleted.is_(False),
                ).order_by(ContentPackage.created_at.desc()).limit(1)
            )
            cp = cp_r.scalar_one_or_none()
            if cp:
                content_package_id = cp.id
        except Exception:
            pass

    content_bytes = await get_file_content_bytes(file_id, owner_id)

    async with AsyncSessionLocal() as db:
        from app.services.file_service import check_file_access
        file = await check_file_access(db, file_id, owner_id)

        artifact = await create_artifact(
            db, owner_id,
            name=file.name,
            extension=file.extension or "",
            content=content_bytes,
            folder_id=file.folder_id,
            source_module="office-gen",
            source_object_type="content_package" if content_package_id else "file",
            source_object_id=content_package_id or file_id,
            file_id=file_id,
            conflict_policy="create_version",
        )

    return {
        "artifact_id": artifact["id"],
        "file_id": file_id,
        "content_package_id": content_package_id,
        "name": artifact["name"],
        "extension": artifact["extension"],
        "size": artifact["size"],
    }


