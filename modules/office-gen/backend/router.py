# ── Helper: persist generated bytes via framework file_upload_service ──

async def _save_file_bytes(
    content: bytes,
    filename: str,
    extension: str,
    owner_id: int,
    folder_id: int | None = None,
) -> dict:
    """Save generated bytes via framework file_upload_service (content-addressed)."""
    ext = extension.lower().lstrip(".")
    final_name = f"{filename}.{ext}"

    async with AsyncSessionLocal() as db:
        result = await upload_file(
            db,
            io.BytesIO(content),
            final_name,
            owner_id,
            folder_id,
        )

    return {
        "file_id": result["id"],
        "name": result["name"],
        "extension": result["extension"],
        "size": result["size"],
        "mime_type": result["mime_type"],
        "storage_path": "",
        "deduplicated": result.get("deduplicated", False),
    }


async def _save_to_artifact(
    content: bytes,
    filename: str,
    extension: str,
    owner_id: int,
    folder_id: int | None = None,
) -> dict:
    """Save generated bytes as an artifact (avoids file-name conflicts)."""
    ext = extension.lower().lstrip(".")
    async with AsyncSessionLocal() as db:
        artifact = await create_artifact(
            db, owner_id,
            name=filename,
            extension=ext,
            content=content,
            folder_id=folder_id,
            source_module="office-gen",
            conflict_policy="create_version",
        )
    return artifact


